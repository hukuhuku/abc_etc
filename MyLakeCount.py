xlen = len(field)
ylen = len(field[0])
Lake_count = 0


def dfs(x,y):
    field[x][y] = '.'
    for dx in [-1,0,1]:
        for dy in [-1,0,1]:
            nx = x + dx
            ny = y + dy
            if (0 <= nx and nx < xlen and 0 <= ny and ny < ylen and field[nx][ny] == 'W'):
                dfs(nx,ny)

for i in range(xlen):
    for j in range(ylen):
        if field[i][j] == 'W':
            dfs(i,j)
            Lake_count += 1


